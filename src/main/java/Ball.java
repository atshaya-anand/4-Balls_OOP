public class Ball {

    public int ball_width;
    public int ball_height;
    public int ball_xPos;
    public int ball_yPos;

    public Ball(int width, int height, int xPos, int yPos){
        ball_width = width;
        ball_height = height;
        ball_xPos = xPos;
        ball_yPos = yPos;
    }

}
